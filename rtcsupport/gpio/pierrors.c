char* getErrorMessage(int error)
{
	int i;

	for (i = 0; i < (sizeof(errInfo) / sizeof(errInfo_t)); i++)
	{
		if (errInfo[i].error == error) 
		{
			return errInfo[i].str;
		}
	}
	return "unknown error";
}